package Study_01;

public class DataType {
	public static void main(String[] args) {
		// 숫자
		// 작은 정수
		int a;
		// 엄청 큰 정수
		long b;
		// 소수점
		float c;
		// 더 긴 소수점
		double d;
		// 아주 작은 숫자 ( 저장공간 아낄때 )
		byte e;
		// int 보다 작은 정수
		short f;

		// 참 거짓
		// true false
		boolean X;

		// 문자
		// 한글자씩 저장
		char A;
		// 여러글자
		String B;	
	}
}
